def is_mainboard_code(code: str) -> bool:
    """沪主板 60、深主板 00（003 也是主板）；排除 300/301 创业板、688/689 科创板、北交所。"""
    return len(code) == 6 and (code.startswith("60") or (code.startswith("00")))


def mainboard_chains() -> tuple[dict[str, list[str]], dict[str, str], list[str]]:
    """返回 (子链→主板代码列表, 代码→名称, 剔除说明)。

    双重保险：板块标签是"主板"且代码前缀也通过 is_mainboard_code 才收录。
    """
    chains: dict[str, list[str]] = {}
    names: dict[str, str] = {}
    dropped: list[str] = []
    for chain, entries in CHAIN_POOL.items():
        keep: list[str] = []
        for name, code, board in entries:
            if code and board == "主板" and is_mainboard_code(code):
                if code not in keep:
                    keep.append(code)
                names[code] = name
            else:
                dropped.append(f"{chain}/{name}({board})")
        if keep:
            chains[chain] = keep
    return chains, names, dropped
